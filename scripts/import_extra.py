def deepseek(prompt, max_tokens=4000):
    resp = requests.post(
        f"{DEEPSEEK_BASE_URL}/chat/completions",
        headers={"Authorization": f"Bearer {DEEPSEEK_API_KEY}", "Content-Type": "application/json"},
        json={"model": DEEPSEEK_MODEL_FAST, "messages": [{"role":"user","content":prompt}], "max_tokens": max_tokens, "temperature": 0.3},
        timeout=180
    )
    if resp.status_code != 200:
        print(f"API Error: {resp.status_code}")
        return ""
    msg = resp.json()["choices"][0]["message"]
    return (msg.get("content") or "") + (msg.get("reasoning_content") or "")


def import_majors_v2():
    """稳健方式：使用DeepSeek分学科门类生成专业"""
    print("📡 分学科门类生成专业数据...")
    
    disciplines = ["哲学","经济学","法学","教育学","文学","历史学","理学","工学","农学","医学","管理学","艺术学"]
    
    total = 0
    for disc in disciplines:
        prompt = f"""请列出"{disc}"学科门类下的所有主要本科专业（2025年版）。

返回严格JSON数组（纯JSON，不要任何markdown标记）：
[
  {{"code":"010101","name":"哲学","category":"哲学类","discipline":"哲学","degree_type":"哲学"}},
  ...
]

字段：code(专业代码6位+后缀), name, category(专业类), discipline(学科门类), degree_type(学位授予门类)
请确保专业代码规范准确。列出该门类下所有重要专业。"""
        
        text = deepseek(prompt, max_tokens=6000)
        if not text:
            print(f"  {disc}: API失败")
            continue
        
        # 提取JSON
        try:
            arr = json.loads(text)
        except:
            # 找最后一个JSON数组
            m = re.findall(r'\[[\s\S]*?\]', text)
            arr = None
            for match in reversed(m):
                try:
                    arr = json.loads(match)
                    break
                except: continue
        
        if not arr:
            print(f"  {disc}: 解析失败")
            continue
        
        cnt = 0
        for item in arr:
            try:
                execute(
                    "INSERT OR IGNORE INTO majors (code,name,category,discipline,degree_type) VALUES (?,?,?,?,?)",
                    (item.get("code",""), item.get("name",""), item.get("category",""),
                     item.get("discipline",disc), item.get("degree_type",""))
                )
                cnt += 1
            except: pass
        
        print(f"  ✅ {disc}: {cnt} 个专业")
        total += cnt
        time.sleep(0.5)
    
    print(f"✅ 专业总计: {total} 个")
    return total


def add_missing_schools():
    """补充缺失省份的高校"""
    existing = set(r["name"] for r in query("SELECT name FROM schools"))
    province_map = {r["name"]: r["id"] for r in query("SELECT id, name FROM provinces")}
    
    # 找出哪些省份的高校较少
    counts = query("""
        SELECT p.name, COUNT(s.id) as c FROM provinces p 
        LEFT JOIN schools s ON p.id=s.province_id 
        GROUP BY p.id ORDER BY c
    """)
    
    need_more = [(r["name"], r["c"]) for r in counts if r["c"] < 15]
    
    if not need_more:
        print("  所有省份高校数量充足")
        return
    
    for prov, cnt in need_more:
        print(f"  补充 {prov} ({cnt}所)...")
        
        prompt = f"""列出{prov}的主要高等院校（本科和专科）。
包括但不限于：985/211/双一流/省属重点/普通本科/知名专科。

返回严格JSON数组（纯JSON，不要markdown）：
[
  {{"name":"XX大学","province":"{prov}","city":"XX市","level":"本科","type":"综合"}},
  ...
]
字段：name, province, city, level(本科/专科), type(综合/理工/师范/医药/农林/财经/政法/语言/艺术/体育/民族)
请列出20所以上。"""
        
        text = deepseek(prompt, max_tokens=6000)
        if not text:
            print(f"    API失败"); continue
        
        try:
            arr = json.loads(text)
        except:
            m = re.findall(r'\[[\s\S]*?\]', text)
            arr = None
            for match in sorted(m, key=len, reverse=True):
                try:
                    arr = json.loads(match)
                    break
                except: continue
        
        if not arr:
            print(f"    解析失败"); continue
        
        added = 0
        pid = province_map.get(prov)
        for item in arr:
            name = item.get("name","")
            if name in existing: continue
            try:
                execute(
                    "INSERT OR IGNORE INTO schools (name,province_id,city,level,type) VALUES (?,?,?,?,?)",
                    (name, pid, item.get("city",""), item.get("level",""), item.get("type",""))
                )
                existing.add(name)
                added += 1
            except: pass
        
        print(f"    ✅ +{added} 所")
        time.sleep(0.5)


def add_descriptions_batch():
    """补充剩余简介"""
    rows = query("SELECT id, name FROM schools WHERE description IS NULL OR description=''")
    if not rows:
        print("  所有高校已有简介")
        return
    
    batch_list = [rows[i:i+25] for i in range(0, len(rows), 25)]
    
    for bidx, batch in enumerate(batch_list):
        names = [r["name"] for r in batch]
        prompt = f"""请为以下高校各写一个20-40字的简介，突出办学层次和优势学科。

返回严格JSON对象（纯JSON，不要markdown）：
{{"XX大学":"简介内容",...}}

{json.dumps(names, ensure_ascii=False)}"""
        
        text = deepseek(prompt, max_tokens=4000)
        if not text:
            print(f"    批次{bidx+1} API失败"); continue
        
        try:
            obj = json.loads(text)
        except:
            m = re.findall(r'\{[\s\S]*?\}', text)
            obj = None
            for match in sorted(m, key=len, reverse=True):
                try:
                    obj = json.loads(match)
                    break
                except: continue
        
        if not obj:
            print(f"    批次{bidx+1} 解析失败"); continue
        
        cnt = 0
        for name, desc in obj.items():
            if isinstance(desc, str) and len(desc) > 5:
                execute("UPDATE schools SET description=? WHERE name=?", (desc, name))
                cnt += 1
        print(f"    ✅ 批次{bidx+1}: {cnt} 所")
        time.sleep(0.3)


